    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        String modeloCarro = textNovoModelo.getText();
        String anoCarro = textAnoCarro.getText();

        String modeloText = textNovoModelo.getText();
        String marcaSelecionada = comboBoxMarca.getSelectedItem().toString();

        DefaultTableModel tabelaCarro = (DefaultTableModel) tblTabelaCarro.getModel();

        Object[] tblCarro = new Object[]{
            marcaSelecionada,
            modeloText,
            anoCarro
        };
        tabelaCarro.addRow(tblCarro);
        textNovoModelo.setText("");
        textAnoCarro.setText("");
    }//GEN-LAST:event_btSalvarActionPerformed


