    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        // TODO add your handling code here:
        
        int linhaSelecionada = tblTabelaCarro.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            JOptionPane.showConfirmDialog(this, "Selecione uma linha para atualizar !");
        } else{
            DefaultTableModel tabelaCarro = (DefaultTableModel) tblTabelaCarro.getModel();
            String marca = tabelaCarro.getValueAt(linhaSelecionada, 0).toString();
            String modeloCarro = tabelaCarro.getValueAt(linhaSelecionada, 1).toString();
            String anoCarro = tabelaCarro.getValueAt(linhaSelecionada, 2).toString();

            textNovoModelo.setText(modeloCarro);
            textAnoCarro.setText(anoCarro);
            comboBoxMarca.setSelectedItem(marca);
            comboBoxMarca.setEnabled(false);
        }


    }//GEN-LAST:event_btAtualizarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:

        int linhaSelectDelete = tblTabelaCarro.getSelectedRow();

        if (linhaSelectDelete == -1) {
            JOptionPane.showConfirmDialog(rootPane, "Selecione uma linha para excluir !");
        } else {
            DefaultTableModel tabelaCarro = (DefaultTableModel) tblTabelaCarro.getModel();

            tabelaCarro.removeRow(linhaSelectDelete);
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btSalvarAtuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarAtuActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblTabelaCarro.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            JOptionPane.showConfirmDialog(this, "Selecione uma linha para atualizar !");
        } else {
        DefaultTableModel tabelaCarro = (DefaultTableModel) tblTabelaCarro.getModel();
        tabelaCarro.setValueAt(comboBoxMarca.getSelectedItem(), linhaSelecionada, 0);
        tabelaCarro.setValueAt(textNovoModelo.getText(), linhaSelecionada, 1);
        tabelaCarro.setValueAt(textAnoCarro.getText(), linhaSelecionada, 2);

        textAnoCarro.setText("");
        textNovoModelo.setText("");
        comboBoxMarca.setEnabled(true);
            
        }
        
    }//GEN-LAST:event_btSalvarAtuActionPerformed

